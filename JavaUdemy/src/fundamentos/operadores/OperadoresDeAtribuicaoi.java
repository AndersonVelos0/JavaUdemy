package fundamentos.operadores;

public class OperadoresDeAtribuicaoi {
	
	public static void main(String[] args) {
		
		int a = 3; 
		int b = a;
		int c = a + b;
		
		
		c += b; // c = c + b
		c -= b; // c = c - b
		c *= b; // c = c * b
		c /= b; // c = c / b
		c %= b; // c = c % b se der 0 é par, se der 1 é impar
		
		
	}

}
