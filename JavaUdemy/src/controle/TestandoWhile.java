package controle;

public class TestandoWhile {
	
	public static void main(String[] args) {
		//While determinado
		int bomDia =  1;
		
		//While irá retornar verdadeiro ou falso 
		//Trata-se de uma variavel de controle que permite efetuar atribuições com indeterminadas vezes
		
		while (bomDia <= 10) {
			System.out.println("Bom dia!");
			bomDia++;
		}
		
	}

}
